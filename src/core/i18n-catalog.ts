/**
 * Chaînes UI (anglais = clés vscode.l10n).
 * Bundles : `l10n/bundle.l10n.json` (en) et `l10n/bundle.l10n.fr.json` (fr) ; autres langues plus tard.
 */
export function escapeHtml(s: string): string {
	return s
		.replace(/&/g, '&amp;')
		.replace(/</g, '&lt;')
		.replace(/>/g, '&gt;')
		.replace(/"/g, '&quot;');
}

