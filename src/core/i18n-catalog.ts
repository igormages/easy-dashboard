export function applyHtmlI18nTokens(content: string, translate: (msg: string) => string): string {
	let out = content;
	for (const [token, msg] of HTML_I18N) {
		out = out.split(token).join(escapeHtml(translate(msg)));
	}
	return out;
}

export function buildWebviewJsI18n(translate: (msg: string) => string): Record<string, string> {
	const o: Record<string, string> = {};
	for (const k of WEBVIEW_JS_KEYS) {
		o[k] = translate(k);
	}
	return o;
}
